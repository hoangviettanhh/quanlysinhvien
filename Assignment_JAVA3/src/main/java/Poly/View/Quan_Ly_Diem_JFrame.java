    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMasv.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn cần nhập vào Mã Sinh Viên");
                return;
            }
            BangDiemDao dao = new BangDiemDao();
            BangDiem bd = dao.fidnByMasv(txtMasv.getText());
            if (bd == null) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên này không tồn tại !!");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa điểm của Sinh Viên này không") == JOptionPane.NO_OPTION) {
                return;
            }
            if (dao.deletebyMasv(txtMasv.getText())) {
                JOptionPane.showMessageDialog(this, "Điểm của Sinh Viên này đã được xóa !!");
            } else {
                JOptionPane.showMessageDialog(this, "Không thể xóa điểm của Sinh Viên này !!");
            }
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error !!" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

