    private void txtTaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTaFocusLost
        // TODO add your handling code here:

        //kiểm tra đk
        if (txtTa.getText().equals("")
                || txtTin.getText().equals("")
                || txtGDTC.getText().equals("")) {
            return;
        }

        float ta = Float.parseFloat(txtTa.getText());
        float tin = Float.parseFloat(txtTin.getText());
        float gdtc = Float.parseFloat(txtGDTC.getText());

        float avg = (ta + tin + gdtc) / 3;

        String st = String.format("%.2f", avg);
        lblDiemTrungbinh.setText(st);
    }//GEN-LAST:event_txtTaFocusLost

