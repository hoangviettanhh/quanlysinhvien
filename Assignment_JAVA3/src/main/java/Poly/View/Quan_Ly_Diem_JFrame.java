    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        //Kiểm tra mã sinh viên
        if (txtMasv.getText().equals("")) {
            sb.append("Mã Sinh Viên NOT NULL !!");
            txtMasv.setBackground(Color.red);
            txtMasv.requestFocus();
        } else {
            txtMasv.setBackground(Color.white);
        }

        //kiểm tra nhập điểm
        if (txtTa.getText().equals("")) {
            sb.append("Tiếng Anh NOT NULL !!");
            txtTa.setBackground(Color.red);

        } else {
            txtTa.setBackground(Color.white);
        }
        if (txtTin.getText().equals("")) {
            sb.append("Tin Học NOT NULL !!");
            txtTin.setBackground(Color.red);

        } else {
            txtTin.setBackground(Color.white);
        }
        if (txtGDTC.getText().equals("")) {
            sb.append("Giáo Dục Thể Chất NOT NULL !!");
            txtGDTC.setBackground(Color.red);

        } else {
            txtGDTC.setBackground(Color.white);
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb);
            return;
        }

        try {
            BangDiem bd = new BangDiem();
            bd.setMasv(txtMasv.getText());
            bd.setTienganh(Float.parseFloat(txtTa.getText()));
            bd.setTinhoc(Float.parseFloat(txtTin.getText()));
            bd.setGdtc(Float.parseFloat(txtGDTC.getText()));

            BangDiemDao dao = new BangDiemDao();

            if (dao.fidnByMasv(txtMasv.getText()) != null) {
                if (JOptionPane.showConfirmDialog(this, "Bạn có muốn cập nhập điểm của Sinh Viên này không") == JOptionPane.NO_OPTION) {
                    return;
                }
                if (dao.update(bd)) {
                    JOptionPane.showMessageDialog(this, "Đã cập nhập lưu điểm của Sinh Viên này");
                } else {
                    JOptionPane.showMessageDialog(this, "Không thể cập nhập điểm của Sinh Viên này !!");
                }
            } else {
                if (dao.insert(bd)) {
                    JOptionPane.showMessageDialog(this, "Đã lưu điểm của Sinh Viên này");
                } else {
                    JOptionPane.showMessageDialog(this, "Không thể lưu điểm của Sinh Viên này !!");
                }
            }
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error !!" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

